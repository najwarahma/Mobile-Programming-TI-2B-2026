void main(){
  double angka1 = 10;
  double angka2 = 5;
  
  print("simple calculator");
  print("Angka: $angka1 dan $angka2");
  
  print("Penjumlahan (+): ${angka1 + angka2}");
  
  print("Pengurangan (-): ${angka1 - angka2}");
  
  print("Perkalian (*): ${angka1 * angka2}");
  
  print("Pembagian (/): ${angka1 / angka2}");
}
